class List extends Array {

    constructor(...args) {
        super(...args);
    }

    /**
     * Add a value at the end of the list.
     * @param val to add
     * @returns index of added value (= last record of the list).
     */
    add(val) {
        this.push(val);
        return this.length-1;
    }

    addFirst(val) {
        this.unshift(val);
        return 0;
    }

    replace(oldVal, val) {
        let i = this.indexOf(oldVal);
        if (i===-1) return undefined;
        this[i] = val;
        return i;
    }

    insert(beforeVal, val) {
        let i = this.indexOf(beforeVal);
        if (i===-1) return undefined;
        this.splice(i, 0, val);
        return i;
    }

    remove(val) {
        let i = this.indexOf(val);
        if (i===-1) return undefined;
        this.splice(i, 1);
        return val;
    }

    delete(val) {
        return this.remove(val);
    }

    contains(val) {
        return this.indexOf(val) >= 0;
    }

    equals(val) {
        if (val.length === undefined || val.length !== this.length) {
            return false;
        }
        for (let i = 0; i < this.length; i++) {
            if (val[i] !== this[i]) {
                return false;
            }
        }
        return true;
    }

    get size() {
        return this.length;
    }

    clear() {
        return (this.length = 0);
    }

    get empty() {
        return this.length === 0;
    }

    duplicate() {
        return this.slice(0);
    }

    indexes() {
        let result = [];
        for (let index in this) {
            result.push(index);
        }
        return result.sort((i1, i2)=>i1-i2);
    }
}

class SpacialSector {

    constructor(locator, parent, x, y, z, width, height, depth, threshold, minSize) {
        this._locator = locator;
        this._parent = parent;
        this._x = x;
        this._y = y;
        this._z = z;
        this._width = width;
        this._height = height;
        this._depth = depth;
        this._threshold = threshold;
        this._minSize = minSize;
    }

    _addInSectors(element) {

        function addInSector(index, dx, dy, dz, element) {
            if (!this._sectors[index]) this._sectors[index] = new SpacialSector(
                this._locator, this,
                this._x+dx, this._y+dy, this._z+dz, this._width/2, this._height/2, this._depth/2,
                this._threshold, this._minSize);
            this._sectors[index].add(element);
        }

        let {x, y, z} = this._locator._coordFinder(element);
        let dx, dy, dz;
        let index=0;
        if (x<this._x) {
            dx = -this._width/4;
        }
        else {
            index+=4; dx = this._width/4;
        }
        if (y<this._y) {
            dy = -this._height/4;
        }
        else {
            index+=2; dy = this._height/4;
        }
        if (z<this._z) {
            dz = -this._depth/4;
        }
        else {
            index+=1; dz = this._depth/4;
        }
        addInSector.call(this, index, dx, dy, dz, element);
    }

    _split() {
        this._sectors = new List();
        for (let element of this._elements) {
            this.add(element);
        }
        delete this._elements;
    }

    add(element) {
        if (this._sectors) {
            this._addInSectors(element);
        }
        else {
            this._locator._elements.set(element, this);
            if (this._elements) {
                this._elements.add(element);
                if (this._elements.length>=this._threshold &&
                    (this._width>this._minSize || this._height>this._minSize || this._depth>this._minSize)) {
                    this._split();
                }
            }
            else {
                this._elements = new List(element);
            }
        }
    }

    _reunite() {
        if (this._sectors) {
            let length = 0;
            for (let index in this._sectors) {
                let sector = this._sectors[index];
                let sectorLength = sector ? sector.length : -1;
                if (!sectorLength) delete this._sectors[index];
                if (sectorLength>0) length += sectorLength;
            }
            if (length<this._threshold) {
                this._elements = new List();
                for (let sector of this._sectors) {
                    if (sector) this._elements.push(...sector.elements);
                }
                delete this._sectors;
                for (let element of this._elements) {
                    this._locator._elements.set(element, this);
                }
            }
        }
    }

    remove(element) {
        this._elements.remove(element);
        if (!this._elements.length) {
            delete this._elements;
        }
        this._locator._elements.delete(element);
        this._parent && this._parent._reunite();
    }

    get elements() {
        if (this._sectors) {
            let elements = new List();
            for (let sector of this._sectors) {
                if (sector) elements.push(...sector.elements);
            }
            return elements;
        }
        else {
            return this._elements ? this._elements : new List();
        }
    }

    get length() {
        if (this._sectors) {
            let length = 0;
            for (let sector of this._sectors) {
                if (sector) length+=sector.length;
            }
            return length;
        }
        else {
            return this._elements ? this._elements.length : 0;
        }
    }

    gather(x, y, z, range, elements) {
        let dx = x-this._x, dy = y-this._y, dz = z-this._z;
        let mx = range+this._width/2,
            my = range+this._height/2,
            mz = range+this._depth/2;
        if (dx*dx+dy*dy+dz*dz<mx*mx+my*my+mz*mz) {
            if (this._sectors) {
                for (let sector of this._sectors) {
                    if (sector) sector.gather(x, y, z, range, elements);
                }
            }
            else if (this._elements) {
                let range2 = range*range;
                for (let element of this._elements) {
                    let coords = this._locator._coordFinder(element);
                    let dx = x-coords.x, dy = y-coords.y, dz = z-coords.z;
                    if (dx*dx+dy*dy+dz*dz<=range2) {
                        elements.add(element);
                    }
                }
            }
        }
    }

}

